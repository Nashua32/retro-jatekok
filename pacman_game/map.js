function drawmap() {
  let size = width/28
  for (let i =0; i<28; i++) {
    for (let j=0; j<28; j++) {
      switch (mapp[i][j]) {
        case 0:
          fill('#df9f78')
          circle((j+0.5)*size, (i+0.5)*size, size-20)
          break
        case 1:
          fill('#1d0238')
          rect(j*size, i*size, size, size)
          break
        case 3:
          fill('#df9f78')
          circle((j+0.5)*size, (i+0.5)*size, size-8)
          break
        default:
          break
      }
    }
  }
}